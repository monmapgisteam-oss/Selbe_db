/**
 * Оноо → түвшний индекс (0 = маш сайн). Өгөгдөлгүй бол −1.
 *
 * Дэлгэц дээр оноог бүхэлчилж харуулдаг тул ангиллыг МӨН бүхэлчилсэн утгаар
 * тогтооно. Эс тэгвээс 84.6 нь "85" гэж харагдаад "Сайн" бүлэгт орж,
 * 85.2 нь мөн "85" гэж харагдаад "Маш сайн" бүлэгт орох зөрчил үүснэ.
 */
export function levelOf(score) {
  if (score === null || score === undefined || !isFinite(score)) return -1;
  const s = Math.round(score);
  return SCORE_LEVELS.findIndex((L) => s >= L.min && s < L.max);
}
